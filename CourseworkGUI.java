    public int countcat(){
        String category = combo2.getSelectedItem().toString();
        int countc = 0;
        
        for(CourseworkTable dd: data){
            if(category.equals(dd.getCategory())){
                cat.add(dd.getTitle());
                countc++;
            }
        }
        return countc;
    }
