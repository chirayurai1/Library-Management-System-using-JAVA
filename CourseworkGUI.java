    public static int binarySearch(ArrayList<CourseworkTable> data, int start, int stop, int search){
        if (start <= stop){
            int middleValue = (start + stop)/2;
            
            if (data.get(middleValue).getPrice() == search){
                    return middleValue;
            }
            else if (data.get(middleValue).getPrice() > search){
                return binarySearch(data, start, middleValue-1, search);
            }
            else{
                return binarySearch(data, middleValue+1, stop, search);
            }
        }
        else{
            return -1;
        }
    }
