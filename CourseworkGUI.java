    public void readCSV() {
        try {
            BufferedReader bffr = new BufferedReader(new FileReader("CW.CSV"));
            String row = "";
            while ((row = bffr.readLine()) != null) {
                String Values[] = row.split(",");
                DefaultTableModel dtm = (DefaultTableModel) table.getModel();
                dtm.addRow(Values);
                CourseworkTable data0 = new CourseworkTable(Values[0], Values[1], Values[2], Values[3],Values[4],Values[5]);
                data.add(data0);

            }
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "File not Found");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "IOExeption occured");
        } catch(ArrayIndexOutOfBoundsException ex){
            JOptionPane.showMessageDialog(this, "No data found");
        }
        
    }
