    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String Serial = txt1.getText();
        String Title = txt2.getText();
        String Author = txt3.getText();
        String Category = combo1.getSelectedItem().toString();
        String price = txt4.getText();
        
        
        if(Serial.isEmpty() || Title.isEmpty() || Author.isEmpty() || combo1.getSelectedIndex() == 0 && bat1.isSelected() == false && bat2.isSelected() == false && bat3.isSelected() == false || price.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please Enter data", "Empty Field", 2);
            return;
        }
        else{
            try{
                Price = Integer.parseInt(price);
                Serials = Long.parseLong(Serial);
                
                int rowCount = table.getRowCount();
                for (int i = 0; i < rowCount; i++) {
                    String seriall = (String) table.getValueAt(i, 0);
                    if (Serial.equals(seriall)) {
                        JOptionPane.showMessageDialog(this, "Serial number already entered.", "Invalid Input", 2);
                        return;
                    }
                } 
                DefaultTableModel dfm = (DefaultTableModel) table.getModel();
                String[] st = {Long.toString(Serials), Title, Author, Category, Value, Integer.toString(Price)};
                dfm.addRow(st);
                CourseworkTable data0 = new CourseworkTable(Long.toString(Serials), Title, Author, Category, Value, Integer.toString(Price));
                data.add(data0);
                loadCSV();
            }
            catch (NumberFormatException e){
                JOptionPane.showMessageDialog(rootPane,"Enter Numerical value","Error",0);
            }
            
        }
                   
    }//GEN-LAST:event_jButton2ActionPerformed

